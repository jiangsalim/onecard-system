"""
Daily email reports for staff based on their roles.
"""
from datetime import date
from django.utils import timezone


def generate_daily_report(user):
    """Generate role-based daily report."""
    today = date.today()
    
    if user.role in ['super_admin', 'admin']:
        return generate_admin_report(today)
    elif user.role == 'bursar':
        return generate_bursar_report(today)
    elif user.role == 'gate_staff':
        return generate_gate_report(today)
    elif user.role == 'class_teacher':
        return generate_teacher_report(user, today)
    
    return None



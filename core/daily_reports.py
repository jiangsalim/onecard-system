def generate_bursar_report(today):
    """Fee-focused report for bursar."""
    from core.models import Student
    from core.services import get_payment_balance
    from fees.models import FeeStructure
    
    students = Student.objects.filter(status='active')
    total_students = students.count()
    
    not_cleared = 0
    total_expected = 0
    total_collected = 0
    
    for s in students[:500]:  # Sample for performance
        paid = float(get_payment_balance(s.payment_code))
        fee = FeeStructure.objects.first()
        total_fee = float(fee.total_fees) if fee else 800000
        balance = total_fee - paid
        
        total_expected += total_fee
        total_collected += paid
